import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const siteRoot = path.resolve(__dirname, '..')
const contentRoot = path.resolve(siteRoot, '..')
const publicDir = path.join(siteRoot, 'public', 'chapters')
const outputFile = path.join(siteRoot, 'src', 'data', 'chapters.ts')

function toSlug(folderName) {
  const match = folderName.match(/^(\d+)\.\s*(.+)$/)
  if (!match) return null
  const num = match[1].padStart(2, '0')
  const name = match[2]
    .trim()
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/(^-|-$)/g, '')
  return `${num}-${name}`
}

function getCategory(order) {
  if (order <= 3) return 'fundamentals'
  if (order <= 7) return 'building-blocks'
  return 'case-studies'
}

function findReadme(dir) {
  for (const name of ['Readme.md', 'README.md', 'readme.md']) {
    const file = path.join(dir, name)
    if (fs.existsSync(file)) return file
  }
  return null
}

function extractTitle(markdown) {
  const match = markdown.match(/^#\s+(.+)$/m)
  return match ? match[1].trim() : 'Untitled'
}

function extractSubtitle(markdown) {
  const lines = markdown.split('\n')
  let pastTitle = false
  for (const line of lines) {
    if (line.startsWith('# ')) {
      pastTitle = true
      continue
    }
    if (!pastTitle) continue
    const trimmed = line.trim()
    if (!trimmed || trimmed === '---') continue
    if (trimmed.startsWith('#')) continue
    return trimmed.replace(/\*\*/g, '').slice(0, 160)
  }
  return ''
}

function estimateReadTime(markdown) {
  const words = markdown.split(/\s+/).filter(Boolean).length
  return Math.max(5, Math.ceil(words / 200))
}

function rewriteImagePaths(markdown, slug) {
  const base = `/chapters/${slug}/images`
  return markdown
    .replace(/src="\.\/images\/\//g, `src="${base}/`)
    .replace(/src="\.\/images\//g, `src="${base}/`)
    .replace(/src='\.\/images\//g, `src='${base}/`)
}

function copyDir(src, dest) {
  fs.mkdirSync(dest, { recursive: true })
  for (const entry of fs.readdirSync(src, { withFileTypes: true })) {
    const srcPath = path.join(src, entry.name)
    const destPath = path.join(dest, entry.name)
    if (entry.isDirectory()) {
      copyDir(srcPath, destPath)
    } else {
      fs.copyFileSync(srcPath, destPath)
    }
  }
}

function countImages(dir) {
  const imagesDir = path.join(dir, 'images')
  if (!fs.existsSync(imagesDir)) return 0
  return fs
    .readdirSync(imagesDir)
    .filter((f) => /\.(png|jpe?g|gif|svg|webp)$/i.test(f)).length
}

const entries = fs
  .readdirSync(contentRoot, { withFileTypes: true })
  .filter((e) => e.isDirectory() && /^\d+\./.test(e.name))
  .sort((a, b) => {
    const na = parseInt(a.name, 10)
    const nb = parseInt(b.name, 10)
    return na - nb
  })

fs.rmSync(publicDir, { recursive: true, force: true })
fs.mkdirSync(publicDir, { recursive: true })

const chapters = []

for (const entry of entries) {
  const sourceDir = path.join(contentRoot, entry.name)
  const slug = toSlug(entry.name)
  if (!slug) continue

  const readmePath = findReadme(sourceDir)
  if (!readmePath) {
    console.warn(`Skipping ${entry.name}: no readme found`)
    continue
  }

  const order = parseInt(entry.name, 10)
  const markdown = fs.readFileSync(readmePath, 'utf-8')
  const destDir = path.join(publicDir, slug)

  copyDir(sourceDir, destDir)

  const rewritten = rewriteImagePaths(markdown, slug)
  fs.writeFileSync(path.join(destDir, 'content.md'), rewritten)

  chapters.push({
    id: slug,
    order,
    title: extractTitle(markdown),
    subtitle: extractSubtitle(markdown),
    category: getCategory(order),
    readTime: estimateReadTime(markdown),
    imageCount: countImages(sourceDir),
    sourceFolder: entry.name,
  })
}

const ts = `// Auto-generated by scripts/sync-chapters.mjs — do not edit manually

export type ChapterCategory = 'fundamentals' | 'building-blocks' | 'case-studies'

export interface Chapter {
  id: string
  order: number
  title: string
  subtitle: string
  category: ChapterCategory
  readTime: number
  imageCount: number
  sourceFolder: string
}

export const categories: { id: ChapterCategory; label: string; description: string }[] = [
  { id: 'fundamentals', label: 'Fundamentals', description: 'Scaling, estimation, and interview framework' },
  { id: 'building-blocks', label: 'Building Blocks', description: 'Rate limiters, hashing, KV stores, and ID generation' },
  { id: 'case-studies', label: 'Case Studies', description: 'End-to-end designs for real-world systems' },
]

export const chapters: Chapter[] = ${JSON.stringify(chapters, null, 2)}

export function getChapterById(id: string): Chapter | undefined {
  return chapters.find((c) => c.id === id)
}

export function getChaptersByCategory(category: ChapterCategory): Chapter[] {
  return chapters.filter((c) => c.category === category)
}
`

fs.writeFileSync(outputFile, ts)
console.log(`Synced ${chapters.length} chapters from ${contentRoot} to public/chapters/`)
